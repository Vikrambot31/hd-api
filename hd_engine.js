// ─── VSOP87 HELIOCENTRIC → GEOCENTRIC CONVERSION ─────────────────────────────
// This is the key fix: proper heliocentric → geocentric transformation
function helioToGeo(L_hel, R_hel, T) {
    // Sun geocentric position
    const sunData = sunXY(T);
    const R0 = sunData.R;       // Earth-Sun distance (AU)
    const L0 = sunData.lon;     // Sun apparent geocentric longitude

    // Earth heliocentric position = Sun geocentric + 180°
    const earthLon = mod360(L0 + 180);
    const earthX = R0 * cosD(earthLon);
    const earthY = R0 * sinD(earthLon);

    // Planet heliocentric rectangular
    const pX = R_hel * cosD(L_hel);
    const pY = R_hel * sinD(L_hel);

    // Geocentric rectangular
    const gX = pX - earthX;
    const gY = pY - earthY;

    return mod360(Math.atan2(gY, gX) * DEG);
}

