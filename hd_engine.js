function calcPluto(T) {
    // Direct lookup from pre-computed table (accurate ±0.5°)
    const idx_f = (T - PLUTO_LON_T0) / PLUTO_LON_STEP;
    const idx   = Math.max(0, Math.min(Math.floor(idx_f), PLUTO_LON.length - 2));
    const alpha = idx_f - idx;
    // Linear interpolation (handle retrograde: use signed difference)
    let v0 = PLUTO_LON[idx], v1 = PLUTO_LON[idx+1];
    let dv = v1 - v0;
    if (dv > 180) dv -= 360;
    if (dv < -180) dv += 360;
    let lon = mod360(v0 + alpha * dv);
    lon = (lon % 360 + 360) % 360;
    return lon;
}

// ─── CHIRON (approximate, ±2°) ───────────────────────────────────────────────
function calcChiron(T) {
    const year = 2000.0 + T * 100;
    const period = 50.7;
    const M = mod360((year - 1996.14) / period * 360);
    const L = mod360(313.5 + (year - 1996.14) * 360/period);
    const e = 0.38;
    const C = 7.5*sinD(M) + 0.5*sinD(2*M);
    const v = mod360(M + C);
    const Lhel = mod360(L + C);
    const R = 13.7 * (1 - e*e) / (1 + e * cosD(v));
    return helioToGeo(Lhel, R, T);
}

