    let L = mod360(252.250906 + 149474.0722491*T + 0.00030350*T*T + 0.000000018*T*T*T);
    const M = mod360(168.6562 + 149474.0705*T);  // mean anomaly
    const e = 0.20563175 - 0.000020407*T;

    // Equation of center
    const C = (23.4400 - 0.0000*T)*sinD(M)
            + 2.9818*sinD(2*M)
            + 0.5255*sinD(3*M)
            + 0.1058*sinD(4*M)
            + 0.0219*sinD(5*M)
