// Sun geocentric rectangular (for planet conversions)
function sunXY(T) {
    const L0 = mod360(280.46646 + 36000.76983 * T + 0.0003032 * T * T);
    const M  = mod360(357.52911 + 35999.05029 * T - 0.0001537 * T * T);
    const e  = 0.016708634 - 0.000042037 * T;
    const C  = (1.9146 - 0.004817*T) * sinD(M) + (0.019993 - 0.000101*T) * sinD(2*M);
    const v  = mod360(M + C);
    const R  = 1.000001018 * (1 - e*e) / (1 + e * cosD(v));
    const sunLon = L0 + C;
    const omega  = mod360(125.04 - 1934.136 * T);
    const appLon = sunLon - 0.00569 - 0.00478 * sinD(omega);
    return { R, lon: appLon };
}

