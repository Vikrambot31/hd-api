// ─── Design Date: Sun exactly 88° before birth Sun ───────────────────────────
function calcDesignJD(birthJD) {
    const T0 = (birthJD - 2451545.0) / 36525.0;
    const birthSun  = calcSun(T0);
    const targetSun = mod360(birthSun - 88.0);
    let jd = birthJD - 88.0;

    for (let i = 0; i < 60; i++) {
        const T = (jd - 2451545.0) / 36525.0;
        const cur = calcSun(T);
        let diff = targetSun - cur;
        if (diff > 180) diff -= 360;
        if (diff < -180) diff += 360;
        if (Math.abs(diff) < 0.000001) break;
        jd += diff / 0.9856;
    }
    return jd;
}

// ─── FullHD Gate/Line/Color/Tone Lookup ──────────────────────────────────────
function lookupGateData(lon) {
    lon = Number(lon);
    lon = (lon % 360 + 360) % 360;
    console.log('lon raw:', lon);
    
    const table = FULL_HD_LOOKUP;
    let lo = 0, hi = table.length - 1;
    let result = table[table.length - 1];
    while (lo <= hi) {
        const mid = (lo + hi) >> 1;
        if (table[mid][0] <= lon) { result = table[mid]; lo = mid + 1; }
        else { hi = mid - 1; }
    }
    return { gate: result[1], line: result[2], color: result[3], tone: result[4] };
}

